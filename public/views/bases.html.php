<p>Sexta.- Sorteo<br>

Con motivo de los cuartos de final de la Champions entre el Real Madrid y el Atlético de Madrid, se creará un microsite para realizar la “PORRA CHAMPIONS”. Los usuarios podrán hacer Su Porra de los partido de Cuartos de final de la champions (el partido de ida y el partido de vuelta),  y se realizarán dos niveles de Sorteos, uno entre los ganadores del resultado de un solo partido y otro sorteo FINAL entre los acertantes de los resultados del partido de ida y el de vuelta.</p>

